import UIKit

extension UIColor {
    
    class func appRed() -> UIColor {
        return UIColor(red: 244/255, green: 47/255, blue: 52/255, alpha: 1)
    }
    
    class func appPurple() -> UIColor {
        return UIColor(red: 241/255, green: 54/255, blue: 187/255, alpha: 1)
    }
}
